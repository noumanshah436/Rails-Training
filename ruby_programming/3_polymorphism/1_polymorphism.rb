# Polymorphism is a made up of two words Poly which means Many and Morph which means Forms. 
# So Polymorphism is a method where one is able to execute the same method using different objects.
#  In polymorphism, we can obtain different results using the same function by passing different input objects.
#  One can also write the If-Else commands but that just makes the code more lengthy.
#   To avoid this, the programmers came up with the concept of polymorphism.

# In Polymorphism, classes have different functionality but they share common interference.

# The concept of polymorphism can be studied under few sub categories.
# 1) Polymorphism using Inheritance
# 2) Polymorphism using Duck-Typing